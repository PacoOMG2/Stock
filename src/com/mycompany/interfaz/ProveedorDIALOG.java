    private void TablaProveedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaProveedorMouseClicked
        
        
        this.btnActualizar.setEnabled(true);
        this.btnEliminar.setEnabled(true);

        
        int fila = this.TablaProveedor.getSelectedRow();

        this.idSeleccionado = Integer.parseInt(this.TablaProveedor.getValueAt(fila, 0).toString());

        System.out.println(this.idSeleccionado);

        try {
            ResultSet resultado = this.crud.seleccionar(SQL.selectProveedor(this.idSeleccionado));

            if (resultado.next()) {

                //this.txtNombre.setText(resultado.getNString());
                //this.boxDepartamento.addItem(resultado.getString(4));
                btnInsertar.setEnabled(false);

            }

        } catch (Exception e) {
            System.out.println("Error" + e.getMessage());
        }

        try {
            ResultSet resultado = this.crud.seleccionar(SQL.selectProveedor(this.idSeleccionado));

            if (resultado.next()) {

                
                this.txtNombre.setText(resultado.getString(2));
                this.txtTelefono.setText(resultado.getString(3));
                
                int id = resultado.getInt(4);
                for (int i=0; 1<this.departamentos.size(); i++){
                    
                    if(this.departamentos.get(i).getId_departamento()==id){
                    this.boxDepartamento.setSelectedItem(this.departamentos.get(i).getNombre());
                    break; 
                    }
                    
                }//cierre for

            }

        } catch (Exception e) {
            System.out.println("Error" + e.getMessage());
        }
    }//GEN-LAST:event_TablaProveedorMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        this.proveedor = new Proveedor();
        
        this.proveedor.setNombre_proveedor(txtNombre.getText());
        this.proveedor.setTelefono(txtTelefono.getText());
        
        int indice = this.boxDepartamento.getSelectedIndex();
        int idDep = this.departamentos.get(indice).getId_departamento();
        this.proveedor.setId_departamento(idDep);
        
        boolean result = this.crud.eliminarInsertar(SQL.actualizarProveedor(proveedor, this.idSeleccionado));
        if (result) {
            JOptionPane.showMessageDialog(null, "Actualizado correctamente");
            this.mostrarProveedor();
            
        } else {
            JOptionPane.showMessageDialog(null, "No actualizo");
        } 
        
        
    }//GEN-LAST:event_btnActualizarActionPerformed

