 private void cargarDepartamentos() {
        this.boxDepartamento.removeAllItems();
        
        try {
            ResultSet rst = this.crud.seleccionar(SQL.seletTodosDepartamento());
            
            Departamento dep;
            
            this.departamentos = new ArrayList();
            
            while (rst.next()) {                
                this.boxDepartamento.addItem(rst.getString(2));
                
               dep = new Departamento();
               
               dep.setId_departamento(rst.getInt(1));
               dep.setNombre(rst.getNString(2));
               
               this.departamentos.add(dep);
                
            }//cierra while
            
        } catch (SQLException e) {
            System.err.println("Error al cargar el departamento" + e.getMessage());
        }
        
    }//Cierro el motodo cargarDepartamentos
    
   
    private void mostrarProveedor() {
        this.limpiarTabla();
        DefaultTableModel modelo = (DefaultTableModel) TablaProveedor.getModel();
        modelo.addColumn("Id Proveedor");
        modelo.addColumn("Nombre");
        modelo.addColumn("Teléfono");
        modelo.addColumn("Departamento");


        try {
            ResultSet resultado = this.crud.seleccionar(SQL.seletTodosProveedores());

            while (resultado.next()) {

                Object[] depAux = {resultado.getInt(1), resultado.getString(2), resultado.getString(3), resultado.getString(4)};//Obtenemos el id y el nombre  
                modelo.addRow(depAux);
            }//cierre de while

            this.TablaProveedor.setModel(modelo);

        } catch (SQLException e) {
            System.out.println("Error:" + e.getMessage());
    
        }
    }

    private void limpiarTabla() {
        TablaProveedor.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{}
        ));
    }
    

