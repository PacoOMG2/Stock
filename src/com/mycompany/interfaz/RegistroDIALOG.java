    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Producto producto = new Producto();

        String nombre = this.txtNombre.getText();
        int cantidad = Integer.parseInt(this.txtCantidad.getText());
        float pc = Float.parseFloat(this.txtPrecioCosto.getText()); //SQUI SE ENCUENTRA EL ERROR
        System.out.println(pc);
        float ganancias = ((Integer) this.porcentaje.getValue());
        float pv = pc + (pc * ganancias / 100);
        System.out.println(pv);
        String fecha = this.txtFecha.getText();
        String factura = this.txtFolio.getText();

        int indice = this.boxDepartamento.getSelectedIndex();

        int idDep = this.departamentos.get(indice).getId_departamento();

        producto.setNombre(nombre);
        producto.setCantidad(cantidad);
        producto.setPrecioCosto(pc);
        producto.setPrecioVenta(pv);
        producto.setFecha(fecha);
        producto.setFolio_factura(factura);
        producto.setId_departamento(idDep);

        boolean resultado = this.crud.insertProducto(producto);

        this.txtNombre.setText("");
        this.txtCantidad.setText("");
        this.txtPrecioCosto.setText("");
        this.txtPrecioVenta.setText("");
        this.txtFecha.setText("");
        this.txtFolio.setText("");
        boxDepartamento.setSelectedIndex(-1);

        if (resultado) {
            JOptionPane.showMessageDialog(null, "Producto insertado correctamente.");
            this.mostrarProductos();
        } else {
            JOptionPane.showMessageDialog(null, "Producto no insertado correctamente.");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        boolean resultado = this.crud.eliminarInsertar(SQL.eliminarProducto(this.idSeleccionado));

        if (resultado) {
            JOptionPane.showMessageDialog(null, "Eliminado correctamente");
            this.mostrarProductos();
        } else {
            JOptionPane.showMessageDialog(null, "No eliminado correctamente");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        this.btnGuardar.setEnabled(false);
        this.btnActualizar.setEnabled(true);
        this.btnEliminar.setEnabled(true);

        int fila = this.jTable1.getSelectedRow();

        this.idSeleccionado = Integer.parseInt(this.jTable1.getValueAt(fila, 0).toString());

        System.out.println(this.idSeleccionado);

        try {
            ResultSet resultado = this.crud.seleccionar(SQL.selectProductosId(this.idSeleccionado));

            if (resultado.next()) {

                this.txtNombre.setText(resultado.getString(2));
                this.txtCantidad.setText(resultado.getString(3));
                this.txtPrecioCosto.setText(resultado.getString(4));
                this.txtPrecioVenta.setText(resultado.getString(5));
                this.txtFecha.setText(resultado.getString(6));
                this.txtFolio.setText(resultado.getString(7));

                //departamento 
                int id = resultado.getInt(8);
                for (int i = 0; 1 < this.departamentos.size(); i++) {

                    if (this.departamentos.get(i).getId_departamento() == id) {
                        this.boxDepartamento.setSelectedItem(this.departamentos.get(i).getNombre());
                        break;
                    }

                }//cierre for

            }

        } catch (SQLException e) {
            System.out.println("Error" + e.getMessage());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        this.producto = new Producto();

        this.producto.setNombre(this.txtNombre.getText());
        this.producto.setCantidad(Integer.parseInt(this.txtCantidad.getText()));

        float pc = Float.parseFloat(this.txtPrecioCosto.getText());
        this.producto.setPrecioCosto(pc);

        float ganancias = ((Integer) this.porcentaje.getValue());
        float pv = pc + (pc * ganancias / 100);
        this.producto.setPrecioVenta(pv);
        this.producto.setFecha(this.txtFecha.getText());
        this.producto.setFolio_factura(this.txtFolio.getText());

        int indice = this.boxDepartamento.getSelectedIndex();
        int idDep = this.departamentos.get(indice).getId_departamento();
        this.producto.setId_departamento(idDep);
        System.out.println("idDep = " + idDep);

        boolean result = this.crud.eliminarInsertar(SQL.actualizarProductos(producto, this.idSeleccionado));

        if (result) {
            JOptionPane.showMessageDialog(null, "Actualizado correctamente");
            this.mostrarProductos();

        } else {
            JOptionPane.showMessageDialog(null, "No actualizo");
        }


    }//GEN-LAST:event_btnActualizarActionPerformed

